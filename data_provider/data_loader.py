class Dataset_Process(Dataset):
    """基础处理类：定义框架与 PSR 逻辑"""
    def __init__(self, root_path, flag='train', size=None,
                 features='S', data_path='ETTh1.csv',
                 target='OT', scale=True, timeenc=0, freq='h', 
                 tau=1, m_dim=3):
        self.seq_len = size[0] if size else 24 * 4 * 4
        self.label_len = size[1] if size else 24 * 4
        self.pred_len = size[2] if size else 24 * 4
        assert flag in ['train', 'test', 'val', 'pred']
        type_map = {'train': 0, 'val': 1, 'test': 2, 'pred': 3}
        self.set_type = type_map[flag]
        self.features, self.target = features, target
        self.scale, self.timeenc, self.freq = scale, timeenc, freq
        self.tau, self.m_dim = tau, m_dim  
        self.root_path, self.data_path = root_path, data_path
        self.scaler = StandardScaler()
        self.__read_data__()

    def __getitem__(self, index):
        s_begin = index
        s_end = s_begin + self.seq_len
        r_begin = s_end - self.label_len
        r_end = r_begin + self.label_len + self.pred_len
        seq_x = self.data_x[s_begin:s_end]
        seq_y = self.data_y[r_begin:r_end]
        seq_x_mark = self.data_stamp[s_begin:s_end]
        seq_y_mark = self.data_stamp[r_begin:r_end]

        # 🚀 统一 PSR 逻辑：零拷贝 Stride Trick
        valid_len = self.seq_len - (self.m_dim - 1) * self.tau
        seq_x_cont = np.ascontiguousarray(seq_x)
        s0, s1 = seq_x_cont.strides 
        x_chaos = np.lib.stride_tricks.as_strided(
            seq_x_cont,
            shape=(valid_len, seq_x_cont.shape[1], self.m_dim),
            strides=(s0, s1, self.tau * s0)
        )
        return seq_x.astype(np.float32), seq_y.astype(np.float32), \
               seq_x_mark.astype(np.float32), seq_y_mark.astype(np.float32), \
               x_chaos.astype(np.float32)

    def __len__(self):
        return len(self.data_x) - self.seq_len - self.pred_len + 1
    def inverse_transform(self, data):
        return self.scaler.inverse_transform(data)

# 🚀 补全 ETT 小时类
class Dataset_ETT_hour(Dataset_Process):
    def __read_data__(self):
        df_raw = pd.read_csv(os.path.join(self.root_path, self.data_path))
        border1s = [0, 12 * 30 * 24 - self.seq_len, 12 * 30 * 24 + 4 * 30 * 24 - self.seq_len]
        border2s = [12 * 30 * 24, 12 * 30 * 24 + 4 * 30 * 24, 12 * 30 * 24 + 8 * 30 * 24]
        border1, border2 = border1s[self.set_type], border2s[self.set_type]
        df_data = df_raw.iloc[:, 1:] if self.features in ['M', 'MS'] else df_raw[[self.target]]
        if self.scale:
            train_data = df_data.iloc[border1s[0]:border2s[0]]
            self.scaler.fit(train_data.values)
            data = self.scaler.transform(df_data.values)
        else: data = df_data.values
        df_stamp = df_raw[['date']][border1:border2]
        df_stamp['date'] = pd.to_datetime(df_stamp.date)
        data_stamp = time_features(pd.to_datetime(df_stamp['date'].values), freq=self.freq).transpose(1, 0)
        self.data_x, self.data_y, self.data_stamp = data[border1:border2], data[border1:border2], data_stamp

